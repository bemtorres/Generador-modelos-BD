-- PROCEDIMIENTOS --

CREATE OR REPLACE PROCEDURE proc_insert_employees(v_employee_id NUMBER,v_first_name VARCHAR2,v_last_name VARCHAR2,v_email VARCHAR2,v_phone_number VARCHAR2,v_hire_date DATE,v_job_id VARCHAR2,v_salary NUMBER,v_commission_pct NUMBER,v_manager_id NUMBER,v_department_id NUMBER)
AS 
BEGIN 
INSERT INTO Employees (EMPLOYEE_ID,FIRST_NAME,LAST_NAME,EMAIL,PHONE_NUMBER,HIRE_DATE,JOB_ID,SALARY,COMMISSION_PCT,MANAGER_ID,DEPARTMENT_ID) VALUES (v_employee_id,v_first_name,v_last_name,v_email,v_phone_number,v_hire_date,v_job_id,v_salary,v_commission_pct,v_manager_id,v_department_id);    
END;

