-- PROCEDIMIENTOS --

CREATE OR REPLACE PROCEDURE proc_insert_mantencion(v_id_mantencion NUMBER,v_id_departamento NUMBER,v_fecha_inicio DATE,v_fecha_termino DATE,v_finalizado CHAR)
AS 
BEGIN 
INSERT INTO Mantencion (ID_MANTENCION,ID_DEPARTAMENTO,FECHA_INICIO,FECHA_TERMINO,FINALIZADO) VALUES (v_id_mantencion,v_id_departamento,v_fecha_inicio,v_fecha_termino,v_finalizado);    
END;

