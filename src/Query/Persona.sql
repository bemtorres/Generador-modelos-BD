CREATE OR REPLACE PROCEDURE proc_insert_persona(v_id_persona NUMBER,v_run VARCHAR2,v_pasaporte VARCHAR2,v_extranjero CHAR,v_nombre VARCHAR2,v_apellido VARCHAR2)
AS 
BEGIN 
INSERT INTO Persona (ID_PERSONA,RUN,PASAPORTE,EXTRANJERO,NOMBRE,APELLIDO) VALUES (v_id_persona,v_run,v_pasaporte,v_extranjero,v_nombre,v_apellido);    
