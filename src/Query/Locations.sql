-- PROCEDIMIENTOS --

CREATE OR REPLACE PROCEDURE proc_insert_locations(v_location_id NUMBER,v_street_address VARCHAR2,v_postal_code VARCHAR2,v_city VARCHAR2,v_state_province VARCHAR2,v_country_id CHAR)
AS 
BEGIN 
INSERT INTO Locations (LOCATION_ID,STREET_ADDRESS,POSTAL_CODE,CITY,STATE_PROVINCE,COUNTRY_ID) VALUES (v_location_id,v_street_address,v_postal_code,v_city,v_state_province,v_country_id);    
END;

